export default function RegionTypeMap({ apiKey }) {
  const mapRef = useRef(null);
  const [mapType, setMapType] = useState("roadmap"); // 'roadmap' | 'satellite'
  const [selectedRegion, setSelectedRegion] = useState(null);
  const [activeProperty, setActiveProperty] = useState(null);

  const { isLoaded } = useJsApiLoader({ googleMapsApiKey: apiKey });

  const onLoad = useCallback((map) => {
    mapRef.current = map;
  }, []);

  const toggleMapType = () =>
    setMapType(mapType === "roadmap" ? "satellite" : "roadmap");

  const handleRegionClick = (region) => {
    console.log("====================================");
    console.log(region.areaID);
    console.log("====================================");
    setSelectedRegion(region);
    if (mapRef.current) {
      const bounds = new window.google.maps.LatLngBounds();
      region.geometry.coordinates.forEach((p) =>
        bounds.extend({ lat: p[1], lng: p[0] })
      );
      mapRef.current.fitBounds(bounds);
    }
  };
  const getPolygonStyle = (region) => ({
    fillColor: "#1E88E5",
    fillOpacity: 0.18,
    strokeColor:
      selectedRegion?.areaID === region.areaID ? "#FF0000" : "#1E88E5", // red if selected
    strokeOpacity: 0.9,
    strokeWeight: 2,
  });

  if (!isLoaded) return <div style={{ padding: 20 }}>Loading map...</div>;

  return (
    <div>
      <button
        onClick={toggleMapType}
        style={{
          position: "absolute",
          zIndex: 10,
          margin: 10,
          padding: "8px 12px",
        }}
      >
        Toggle Map Type: {mapType}
      </button>

      <GoogleMap
        mapContainerStyle={containerStyle}
        center={{ lat: 30.01, lng: 31.45 }}
        zoom={11}
        onLoad={onLoad}
        mapTypeId={mapType}
        options={{
          disableDefaultUI: true,
          zoomControl: true,
          gestureHandling: "greedy",
        }}
      >
        {REGIONS.map((region) => (
          <Polygon
            key={region.id}
            paths={region.geometry.coordinates[0].map(([lng, lat]) => ({
              lat,
              lng,
            }))}
            options={getPolygonStyle(region)}
            onClick={() => handleRegionClick(region)}
          />
        ))}

        <MarkerClusterer>
          {(clusterer) =>
            MOCK_PROPERTIES.filter(
              (p) => !selectedRegion || p.regionId === selectedRegion.id
            ).map((property) => (
              <Marker
                key={property.id}
                position={{ lat: property.lat, lng: property.lng }}
                clusterer={clusterer}
                onClick={() => setActiveProperty(property)}
              />
            ))
          }
        </MarkerClusterer>

        {activeProperty && (
          <InfoWindow
            position={{ lat: activeProperty.lat, lng: activeProperty.lng }}
            onCloseClick={() => setActiveProperty(null)}
          >
            <div style={{ minWidth: 180 }}>
              <h4 style={{ margin: "0 0 5px 0" }}>{activeProperty.title}</h4>
              <strong>{activeProperty.price}</strong>
            </div>
          </InfoWindow>
        )}
      </GoogleMap>
    </div>
  );
}
